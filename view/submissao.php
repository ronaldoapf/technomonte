                        </h3>
                    </div>
                </div>

                <div class="text-left">
                    <div class="col-12">
                        <h4>Datas importantes:</h4>
                        <ul>
                            <li>Data limite para submissão: <b>19/09/2019</b></li>
                            <li>Divulgação dos trabalhos aprovados: 23/09/2019</li>
                            <li>Data limite para inscrição de um autor no evento: 25/09/2019</li>
                            <li>Apresentação dos pôsteres: 02/10/2019, às 18h45, na UNIFUCAMP.</li>

                        </ul>
                    </div>

                </div>

                <div class="col-12">
                    <h4>Comitê Avaliador:</h4>
                    Em breve!
                </div>

                <br>

                <div class="col-12">
                    <h5>1. Definição</h5>
                    <p style="text-align: justify;">
                        1.1.O TECHNOMONTE é um evento Científico e Tecnológico promovido pela
                        UFU e UNIFUCAMP, que tem como objetivo divulgar os resultados dos projetos
                        de pesquisas científicas e tecnológicas realizadas por estudantes de graduação
                        da UFU e UNIFUCAMP, bem como contribuir para a formação dos participantes.

                    </p>
                    <p>
                        1.2.O TECHNOMONTE 2019 ocorrerá entre os dias 01/10/2019 e 03/10/2019,
                        com sessões de apresentação de pôsteres, palestras, minicursos, oficinas e
                        maratonas de programação.
                    </p>
                </div>

                <br>

                <div class="col-12">
                    <h5>2. Da Inscrição e Submissão dos Pôsteres</h5>
                    <p style="text-align: justify;">
                        2.1. Podem participar do TECHNOMONTE estudantes de graduação da UFU e
                        da UNIFUCAMP que tenham desenvolvido projetos de origem científica ou
                        tecnológica. Projetos em andamento também poderão ser submetidos, desde
                        que apresentem algum resultado ou análise preliminar em relação aos
                        objetivos apresentados.
                    </p>
                    <p>
                        2.2. Serão aceitos trabalhos de conclusão de curso, iniciação científica, e de
                        disciplinas da graduação, desde que estejam relacionados a algum tópico da
                        computação.
                    </p>
                    <p style="text-align: justify;">
                        2.3. As submissões devem ser realizadas no site
                        <a target="_blank" href="https://cmt3.research.microsoft.com/Technomonte2019">clique aqui</a>,
                        no período de
                        03/09/2019 à 19/09/2019.</p>
                    <p style="text-align: justify;">
                        2.4. A submissão deverá ser realizada pelo estudante, que será responsável pelas
                        informações prestadas.
                    </p>
                    <p style="text-align: justify;">
                        2.5. Cada estudante poderá inscrever apenas um trabalho.</p>
                    <p style="text-align: justify;">
                        2.6. O estudante que pretende submeter trabalho deverá realizar a sua inscrição no
                        evento até o dia 25/09, no site <a href="https://www.technomonte.com.br/">https://www.technomonte.com.br</a>.
                    </p>
                    <p style="text-align: justify;">
                        2.7. Além do preenchimento dos dados do formulário e do resumo do trabalho,
                        deverá ser anexado um arquivo em formato PDF, contendo o pôster para
                        apresentação do trabalho. As orientações para redação e formatação do pôster
                        constam no <strong><a href="https://www.technomonte.com.br/assets/mostra/Anexo%201.png">Anexo I</a></strong>.
                    </p>
                    <p style="text-align: justify;">
                        2.8. Não serão aceitas inscrições com resumos e pôsteres que estejam fora do
                        padrão solicitado.
                    </p>
                    <p style="text-align: justify;">
                        2.9. O título do trabalho deverá ser o mesmo no formulário de inscrição, no
                        sistema de submissão e no pôster.
                    </p>
                    <p style="text-align: justify;">
                        2.10. A impressão dos pôsteres aprovados para apresentação será de
                        responsabilidade da Comissão Organizadora, desde que o aluno cumpra todos
                        os requisitos exigidos relacionados aos prazos de submissão e inscrição.
                    </p>
                    <p style="text-align: justify;">
                        2.11. É de responsabilidade do estudante acompanhar a validação de sua
                        inscrição, bem como as informações do evento em seu e-mail. Caso não receba
                        as informações, deverá entrar em contato com a Coordenação da Mostra de
                        Trabalhos.
                    </p>
                </div>

                <br>

                <div class="col-12">
                    <h5>3. Da Ausência, Avaliação e Premiação dos Pôsteres</h5>
                    <p style="text-align: justify;">
                        3.1. O estudante que não puder comparecer na data do evento deverá justificar sua
                        ausência à Coordenação da Mostra de Trabalhos até a data do evento.
                    </p>

                    <p style="text-align: justify;">
                        3.2. Os trabalhos serão avaliados por docentes da UFU e da UNIFUCAMP convidados
                        pela Coordenação da Mostra de Trabalhos, de acordo com os critérios no
                        <a href="https://www.technomonte.com.br/assets/mostra/Anexo%202.png"><strong>Anexo II</strong></a>.
                    </p>

                    <p style="text-align: justify;">
                        3.3. Um comitê julgador composto por docentes da UFU e UNIFUCAMP, indicados
                        pela Coordenação da Mostra de Trabalhos, selecionará trabalhos com
                        reconhecido destaque para premiação.
                    </p>
                </div>

